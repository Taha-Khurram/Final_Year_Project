/**
 * Homepage JavaScript
 * Handles homepage-specific interactions
 */

document.addEventListener('DOMContentLoaded', function() {
    // Smooth scroll for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            const href = this.getAttribute('href');
            if (href !== '#') {
                e.preventDefault();
                const target = document.querySelector(href);
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            }
        });
    });

    // Animate elements on scroll
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate-in');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    // Observe feature cards
    document.querySelectorAll('.home-feature-card').forEach(card => {
        observer.observe(card);
    });

    // Observe testimonial cards
    document.querySelectorAll('.home-testimonial-card').forEach(card => {
        observer.observe(card);
    });

    // Observe blog cards
    document.querySelectorAll('.home-blog-card').forEach(card => {
        observer.observe(card);
    });
});
